# ## 6. Augmented inverse propensity-weighted (AIPW) estimator {#aipw}
# 
# The next **augmented inverse propensity-weighted estimator** (AIPW) of the treatment effect is available under unconfoundedness \@ref(eq:cond-unconf) and overlap \@ref(eq:overlap):
# \begin{equation}
#   (\#eq:aipw)
#   \begin{aligned}
#   \htau^{AIPW} &:=
#     \frac{1}{n} \sum_{i=1}^{n}
#       \hmu^{-i}(X_i, 1) - \hmu^{-i}(X_i, 0) \\
#       &+
#       \frac{W}{\he^{-i}(X_i)}  \p{ Y_i - \hmu^{-i}(X_i, 1)}
#       -
#       \frac{1-W}{1-\he^{-i}(X_i)}  \p{ Y_i - \hmu^{-i}(X_i, 0)}
#     \end{aligned}
# \end{equation}
# 
# Letâ€™s parse \@ref(eq:aipw). Ignoring the superscripts for now, the first two terms correspond to an estimate of the treatment effect obtained via direct estimation. The next two terms resemble the IPW estimator, except that we replaced the outcome $Y_i$ with the residuals $Y_i - \hmu(X_i, W_i)$. At a high level, the last two terms estimate and subtract an estimate of the bias of the first.
# 
# The superscripts have to do with a technicality called **cross-fitting** that is required to prevent a specific form of overfitting and allow certain desirable asymptotic properties to hold. That is, we need to fit the outcome and propensity models using one portion of the data, and compute predictions on the remaining portion. An easy way of accomplishing this is to divide the data into K folds, and then estimate $K$ models that are fitted on $K-1$ folds, and then compute predictions on the remaining Kth fold. The example below does that with $K=5$. Note that this is different from cross-validation: the goal of cross-validation is to obtain accurate estimates of the loss function for model selection, whereas the goal of cross-fitting is simply to not use the same observation to fit the model and produce predictions.
# 
# The AIPW estimator \@ref(eq:aipw) has several desirable properties. First, it will be unbiased provided that either $\hmu(X_i, w)$ or $\he(X_i, w)$ is unbiased. Second, it will be consistent provided that either  $\hmu(X_i, w)$ or $\he(X_i, w)$ is consistent. This property is called **double robustness (DR)**, because only one of the outcome or propensity score models needs to be correctly specified, so the estimator is "robust" to misspecification in the remaining model. Under mild assumptions, it is also asymptotically normal and it is efficient, that is has the smallest asymptotic variance than any other estimator. In fact, it even has smaller variance than the difference-in-means estimators in the _randomized_ setting and is therefore superior to it.
# 
# The next snippet provides an implementation of the AIPW estimator where outcome and propensity models are estimated using generalized linear models with splines (via `glmnet` and `splines` packages).
# 

